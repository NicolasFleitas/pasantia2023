let a = 3 , b = 2 ;
let z = a + b;


//Incremento 
//Pre- Incremento (el operado ++ antes de la variable)

z = ++a;
console.log(a);
console.log(z);


//Post - Incrementpo (el operador despues del ++ de la variable)

z = b++;

console.log(b);
console.log(z);



//Decremeto
//Predeterminado 

z = --a
console.log(a);
console.log(z);

//Post decremento 

z = b--;
console.log(b);
console.log(z);